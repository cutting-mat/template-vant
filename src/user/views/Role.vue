<template>
  <div v-loading="loading">
    <ToolBar>
      <van-button
        v-auth="role.add"
        type="primary"
        size="small"
        icon="el-icon-plus"
        @click="dialogVisible = true"
      >
        添加
      </van-button>
    </ToolBar>

    <!-- list -->
    <van-table :data="list" style="width: 100%">
      <van-table-column
        prop="name"
        label="角色名称"
        align="center"
      ></van-table-column>
      <van-table-column prop="remark" label="备注"></van-table-column>

      <van-table-column label="操作" width="300" align="center">
        <template slot-scope="scope">
          <van-button
            v-auth="role.edit"
            size="mini"
            type="info"
            plain
            @click="edit(scope.row)"
            >编辑</van-button
          >
          <van-button
            v-auth="role.remove"
            size="mini"
            type="danger"
            plain
            @click="remove(scope.row)"
            >删除</van-button
          >
        </template>
      </van-table-column>
    </van-table>

    <!-- 弹窗 -->
    <van-dialog
      :close-on-click-modal="false"
      class="comDialog"
      title="权限信息"
      :visible="dialogVisible"
      width="600px"
      @close="handleCloseDialog"
    >
      <van-form
        size="small"
        ref="editForm"
        :rules="rules"
        :model="editForm"
        label-width="80px"
      >
        <van-form-item label="角色名称" prop="name">
          <van-field v-model.trim="editForm.name" :maxlength="100"></van-field>
        </van-form-item>
        <van-form-item label="备注" prop="remark">
          <van-field
            v-model.trim="editForm.remark"
            type="textarea"
            :maxlength="1000"
          ></van-field>
        </van-form-item>

        <van-form-item label="权限">
          <TheResourcePicker
            picker
            :checked="editForm.resources"
            @check="editForm.resources = $event.map((e) => e.id)"
          />
        </van-form-item>
      </van-form>
      <span slot="footer" class="dialog-footer">
        <van-button type="primary" @click="save">确 定</van-button>
        <van-button @click="dialogVisible = false">取 消</van-button>
      </span>
    </van-dialog>
  </div>
</template>

<script>
import { deepcopy } from "@/core";
import * as role from "../api/role";

export default {
  components: {
    TheResourcePicker: () => import("@/user/components/TheResourcePicker.vue"),
  },
  data() {
    return {
      role,
      dialogVisible: false,
      loading: false,
      list: [],
      editForm: {
        name: "",
        remark: "",
        resources: [],
      },
      rules: {
        name: [
          { required: true, message: "请输入角色名称", trigger: "blur" },
          {
            min: 1,
            max: 100,
            message: "长度 1 到 100 个字符",
            trigger: "blur",
          },
        ],
        remark: [
          {
            min: 0,
            max: 255,
            message: "长度 0 到 255 个字符",
            trigger: "blur",
          },
        ],
      },
    };
  },
  methods: {
    edit(data) {
      this.editForm = deepcopy(data);
      this.$set(this.editForm, "resources", data.resources);
      this.dialogVisible = true;
    },
    save() {
      // 表单验证
      this.$refs["editForm"].validate((valid) => {
        if (valid) {
          this.loading = true;
          let formData = deepcopy(this.editForm);
          this.handleCloseDialog();
          let doAction = !formData.id ? role.add : role.edit;
          // 分类型提交
          doAction(formData)
            .then(() => {
              this.fetchData();
              this.$message({
                message: "添加成功",
                type: "success",
              });
            })
            .catch(() => {
              this.loading = false;
            });
        }
      });
    },
    handleCloseDialog: function () {
      this.dialogVisible = false;
      this.editForm = {
        name: "",
        remark: "",
        resources: [],
      };
      this.$refs.editForm && this.$refs.editForm.resetFields();
    },
    remove(item) {
      if (!item || !item.id) {
        return null;
      }
      this.$confirm("是否删除?", "提示", {
        confirmButtonText: "确定",
        cancelButtonText: "取消",
        type: "warning",
      }).then(() => {
        this.loading = true;
        role
          .remove({
            id: item.id,
          })
          .then(() => {
            this.fetchData();
            this.$message({
              message: "删除成功",
              type: "success",
            });
          })
          .catch(() => {
            this.loading = false;
          });
      });
    },
    fetchData: function () {
      this.loading = true;
      role
        .list()
        .then((res) => {
          this.loading = false;
          this.list = res.data;
        })
        .catch(() => {
          this.loading = false;
        });
    },
  },
  created: function () {
    this.fetchData();
  },
};
</script>

